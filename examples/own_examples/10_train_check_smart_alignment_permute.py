def format_bits(pulses):
    bit_string = "".join(str(int(bit)) for bit in pulses)

    return " ".join(
        bit_string[start:start + BITS_PER_GROUP]
        for start in range(0, len(bit_string), BITS_PER_GROUP)
    )


def format_group_layout(group_ids):
    """Format group IDs as A, B, ..., Z, G26, G27, ..."""
    labels = []

    for group_id in group_ids:
        group_id = int(group_id)
        if group_id < 26:
            labels.append(chr(ord("A") + group_id))
        else:
            labels.append(f"G{group_id}")

    return " ".join(labels)


def print_matrix(name, signs, pulses):
    for feature_idx in range(pulses.shape[0]):
        print(
            f"  {name}{feature_idx:<3} "
            f"s={int(signs[feature_idx])} "
            f"{format_bits(pulses[feature_idx])}"
        )


def inspect_batch_matrix(name, signs, pulses):
    (
        status,
        matched_columns,
        _,
        group_sizes,
        group_ids,
    ) = analyze_repeated_group_encoding(pulses)

    print(
        f"{name}: {status} "
        f"({matched_columns}/{pulses.shape[1]} columns match)"
    )
    print(f"  Group sizes:  {group_sizes}")
    print(f"  Burst layout: {format_group_layout(group_ids)}")

    print_matrix(name, signs, pulses)

    return status



# ============================================================
# Main
# ============================================================


def main() -> None:
    args = parse_arguments()

    x_train, d_train, metadata = load_train_dump(args.dump_path)

    batch_size = int(metadata["batch_size"])
    input_count = int(metadata["input_count"])
    output_count = int(metadata["output_count"])
    bl = int(metadata["bl"])
    stored_words_per_train = int(metadata["words_per_train"])
    out_trans = bool(int(metadata["out_trans"]))

    words_per_train = words_from_bl(bl)

    if words_per_train != stored_words_per_train:
        raise ValueError(
            f"BL={bl} requires {words_per_train} words/train, "
            f"but the dump stores {stored_words_per_train}."
        )

    expected_x_length = input_count * batch_size * words_per_train
    expected_d_length = output_count * batch_size * words_per_train

    if len(x_train) != expected_x_length:
        raise ValueError(
            f"x_train has {len(x_train)} values, "
            f"expected {expected_x_length} from the stored metadata."
        )

    if len(d_train) != expected_d_length:
        raise ValueError(
            f"d_train has {len(d_train)} values, "
            f"expected {expected_d_length} from the stored metadata."
        )

    print("\nStored configuration")
    print(f"  Batch size:       {batch_size}")
    print(f"  Inputs:           {input_count}")
    print(f"  Outputs:          {output_count}")
    print(f"  BL:               {bl}")
    print(f"  Words per train:  {words_per_train}")
    print(f"  out_trans:        {int(out_trans)}")

    if "granularity" in metadata:
        print(f"  Granularity:      {metadata['granularity']}")

    summary = {
        "X": Counter(),
        "D": Counter(),
    }

    print("\nPulse order: pulse 0 -> pulse BL-1")
    print("Signs are displayed separately and excluded from the check.")
    print(
        "A valid permutation must move complete columns identically across "
        "all rows.\n"
    )

    for batch_idx in range(batch_size):
        print(f"--- Batch {batch_idx} ---")

        x_signs, x_pulses = reconstruct_batch(
            train=x_train,
            batch_idx=batch_idx,
            feature_count=input_count,
            words_per_train=words_per_train,
            batch_size=batch_size,
            bl=bl,
            out_trans=out_trans,
        )

        d_signs, d_pulses = reconstruct_batch(
            train=d_train,
            batch_idx=batch_idx,
            feature_count=output_count,
            words_per_train=words_per_train,
            batch_size=batch_size,
            bl=bl,
            out_trans=out_trans,
        )

        x_status = inspect_batch_matrix(
            name="X",
            signs=x_signs,
            pulses=x_pulses,
        )

        d_status = inspect_batch_matrix(
            name="D",
            signs=d_signs,
            pulses=d_pulses,
        )

        summary["X"][x_status] += 1
        summary["D"][d_status] += 1

        print()

    print("Summary")

    for name in ("X", "D"):
        print(
            f"{name}: "
            f"{summary[name]['GROUP BURST EXACT']} exact burst, "
            f"{summary[name]['GROUP PERMUTED OK']} valid permutation, "
            f"{summary[name]['INVALID']} invalid"
        )


if __name__ == "__main__":
    main()